export function referenceMetadata(source) {
  if (!source) return {}
  return {
    sourceReference: source.name,
    sourceLineage: source.lineage,
    sourceCitation: source.citation,
    sourceUrl: source.url,
    sourceDownloadUrl: source.downloadUrl,
    sourceDownloadHint: source.downloadHint,
    sourceFormat: source.preferredFormat,
    sourceFocus: source.focus,
    installation: source.installation,
    loadType: source.loadType,
  }
}

export function sourceTypeForImport(format, source) {
  if (source?.mode === 'demo') return 'Demonstração calculada'
  if (source?.lineage) return `${source.lineage} importada (${format})`
  return `Arquivo importado (${format})`
}
